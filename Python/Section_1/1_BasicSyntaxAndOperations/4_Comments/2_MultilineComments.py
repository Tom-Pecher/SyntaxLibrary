
## Python: 1.1.4.2 - Multiline Comments


# There are no multi-line comments in Python.
# We can use triple quotes to create a multi-line comment:
"""
This is a
multi-line comment
"""
# However, unlike true comments, this takes up memory in code.

# It is often better to use multiple single-line comments:
# This is a multi-line comment
# written using several
# single-line comment symbols.
